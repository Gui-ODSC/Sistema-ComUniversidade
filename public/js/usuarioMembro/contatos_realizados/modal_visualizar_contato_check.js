function openModalVisualizarContatoRealizadoCheck() {
    // Exibe o modal e a sobreposição
    document.getElementById('modal-visualizar-contato-check').style.display = 'block';
    document.getElementById('clicar-fora-modal-visualizar-contato-check').style.display = 'block';
}

function closeModalVisualizarContatoRealizadoCheck() {
    // Oculta o modal e a sobreposição
    document.getElementById('modal-visualizar-contato-check').style.display = 'none';
    document.getElementById('clicar-fora-modal-visualizar-contato-check').style.display = 'none';
}


