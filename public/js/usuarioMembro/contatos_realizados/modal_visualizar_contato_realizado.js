function openModalVisualizarContatoRealizado() {
    // Exibe o modal e a sobreposição
    document.getElementById('modal-visualizar-contato-realizado').style.display = 'block';
    document.getElementById('clicar-fora-modal-visualizar-contato-realizado').style.display = 'block';
}

function closeModalVisualizarContatoRealizado() {
    // Oculta o modal e a sobreposição
    document.getElementById('modal-visualizar-contato-realizado').style.display = 'none';
    document.getElementById('clicar-fora-modal-visualizar-contato-realizado').style.display = 'none';
}


